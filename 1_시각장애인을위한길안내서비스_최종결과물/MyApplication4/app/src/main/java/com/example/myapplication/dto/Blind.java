package com.example.myapplication.dto;

public class Blind {

    public String username;
    public String age;
    public String gender;
    public String uid;

    public Blind() {
    }

    public Blind(String username, String age, String gender, String uid) {
        this.username = username;
        this.age = age;
        this.gender = gender;
        this.uid = uid;
    }
}
